/* Render text into a pixel buffer (37×8, values 0 or brightness).
 * start_col: starting column (use 9 to skip RGBW section for cleaner display) */
/* Render text: Sections 1+2 (cols 0-20) reversed → mirrored glyphs.
 * Section 3 (cols 21+) forward → normal glyphs.
 * Glyph direction checked PER PIXEL, not per character. */
static void render_text_at(const char *text, unsigned char pixels[8][37],
                           unsigned char brightness, int start_col) {
    memset(pixels, 0, 8 * 37);
    int x = start_col;
    while (*text && x < 37) {
        unsigned char ch = *text++;
        if (ch >= 128) continue;
        const unsigned char *glyph = font5x7[ch];
        int col;
        for (col = 0; col < 5 && x < 37; col++, x++) {
            unsigned char bits = glyph[col];
            int row;
            for (row = 0; row < 7; row++) {
                if (bits & (1 << row))
                    pixels[row + 1][x] = brightness; /* +1 for vertical center */
            }
        }
        x++;
    }
}

static void render_text(const char *text, unsigned char pixels[8][37], unsigned char brightness) {
    memset(pixels, 0, 8 * 37);
    int x = 0;
    while (*text && x < 37) {
        unsigned char ch = *text++;
        if (ch >= 128) continue;
        const unsigned char *glyph = font5x7[ch];
        int col;
        for (col = 0; col < 5 && x < 37; col++, x++) {
            unsigned char bits = glyph[col];
            int row;
            for (row = 0; row < 7; row++) {
                if (bits & (1 << row))
                    pixels[row + 1][x] = brightness; /* +1 for vertical center */
            }
        }
        x++; /* 1-pixel gap between chars */
    }
}

